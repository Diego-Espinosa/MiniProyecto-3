    private int obtenerCodigoSeleccionado() {
        String codigoString = JOptionPane.showInputDialog("Ingrese el cÃ³digo del Libro:");
        int codigo = -1;
        try {
            codigo = Integer.parseInt(codigoString);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "CÃ³digo invÃ¡lido", "Error", JOptionPane.ERROR_MESSAGE);
        }
        return codigo;
    }
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String titulo = titulos.getText();
        String autor = autores.getText();
        String genero = Generos.getText();
        int codigo = obtenerCodigoSeleccionado();

String ejemplaresText = ejemplares.getText();
int cantidad;

if (!ejemplaresText.isEmpty()) {
    cantidad = Integer.parseInt(ejemplaresText);
} else {
    cantidad = 0; // Valor predeterminado cuando el campo de texto estÃ¡ vacÃ­o
}

        if (codigo != -1) {
            libroController.actualizarLibro(codigo, genero, titulo, autor, cantidad);
            actualizarLibro();
        }
        titulos.setText("");
        autores.setText("");
        Generos.setText("");
        ejemplares.setText("");
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int codigo = obtenerCodigoSeleccionado();
        if (codigo != -1) {
            libroController.eliminarLibro(codigo);
            actualizarLibro();

        }
    }//GEN-LAST:event_jButton3ActionPerformed

