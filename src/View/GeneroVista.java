    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String codigo = jTextField4.getText().trim();
        if (!codigo.isEmpty()) {
            Libro libro = libroController.obtenerLibroPorCodigo(Integer.parseInt(codigo));
            if (libro != null) {
                // Mostrar los detalles del usuario encontrado en los campos de texto
                titulos.setText(libro.getTitulo());
                autores.setText(libro.getAutor());
                Generos.setText(libro.getGenero());
                ejemplares.setText("" + libro.getEjemplares());
            } else {
                JOptionPane.showMessageDialog(this, "Libro no encontrado", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }    }//GEN-LAST:event_jButton5ActionPerformed

