    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String titulo = titulos.getText();
        String autor = autores.getText();
        String genero = Generos.getText();
String ejemplaresText = ejemplares.getText();
int cantidad;

if (!ejemplaresText.isEmpty()) {
    cantidad = Integer.parseInt(ejemplaresText);
} else {
    cantidad = 0; // Valor predeterminado cuando el campo de texto estÃ¡ vacÃ­o
}

        libroController.agregarLibro(genero, titulo, autor, cantidad);
        actualizarLibro();
        titulos.setText("");
        autores.setText("");
        Generos.setText("");
        ejemplares.setText("");

    }//GEN-LAST:event_jButton1ActionPerformed

